def test_property14_market_data_403_hint():
    """When a market data tool gets a 403 ServerException, handle_sdk_exception
    returns the subscription guidance text."""
    server_exc = _MockServerException(
        http_status=403, error_code="FORBIDDEN", error_msg=_NO_ACCESS_MSG
    )
    with _patch_exceptions():
        result = handle_sdk_exception(server_exc, "get_stock_snapshot", "us")
    assert _get_market_data_hint("us") in result


