_NO_ACCESS_MSG = "no access"

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _config(**overrides) -> ServerConfig:
    defaults = dict(app_key="test_key", app_secret="test_secret")
    defaults.update(overrides)
    return ServerConfig(**defaults)


# ---------------------------------------------------------------------------
# Mock exceptions
# ---------------------------------------------------------------------------

class _MockServerException(Exception):
    """Stand-in for webull ServerException."""

    def __init__(self, http_status=500, error_code="ERR", error_msg="fail"):
        self.http_status = http_status
        self.error_code = error_code
        self.error_msg = error_msg
        super().__init__(error_msg)


class _MockClientException(Exception):
    """Stand-in for webull ClientException."""

    def __init__(self, error_code="PARAM_ERR", error_msg="bad param"):
        self.error_code = error_code
        self.error_msg = error_msg
        super().__init__(error_msg)


def _patch_exceptions():
    """Return a context manager that patches webull exception classes."""
    fake_exceptions = MagicMock()
    fake_exceptions.ServerException = _MockServerException
    fake_exceptions.ClientException = _MockClientException
    return patch.dict(
        "sys.modules",
        {"webull.core.exception.exceptions": fake_exceptions},
    )


# ---------------------------------------------------------------------------
# Property 13: SDK Exception No Crash
# **Validates: Requirements 16.1, 16.2, 16.4, 3.3**
# ---------------------------------------------------------------------------

@pytest.mark.parametrize(
    "exc_factory,expected_fragment",
    [
        (
            lambda: _MockServerException(500, "SVR_ERR", "internal"),
            "Server error",
        ),
        (
            lambda: _MockClientException("CLI_ERR", "bad input"),
            "Parameter error",
        ),
    ],
    ids=["ServerException", "ClientException"],
)
def test_property13_handle_sdk_exception(exc_factory, expected_fragment):
    """handle_sdk_exception returns a structured error string for both
    ServerException and ClientException."""
    exc = exc_factory()
    with _patch_exceptions():
        result = handle_sdk_exception(exc, "test_tool")
    assert isinstance(result, str)
    assert expected_fragment in result


def test_property13_sdk_exception_via_registered_tool():
    """When SDK raises inside a registered tool, the tool returns an error
    string instead of crashing. Tests via the cancel_order tool."""
    pytest = __import__("pytest")
    fastmcp = pytest.importorskip("fastmcp")
    FastMCP = fastmcp.FastMCP

    sdk = MagicMock()
    sdk.trade.order_v3.cancel_order.side_effect = _MockServerException(
        500, "SVR_ERR", "SDK boom"
    )
    audit = AuditLogger(_config())

    mcp = FastMCP("test")
    from webull_openapi_mcp.tools.trading.order import register_order_tools
    register_order_tools(mcp, sdk, audit)

    # Get the registered tool function via public API
    tool = asyncio.get_event_loop().run_until_complete(mcp.get_tool("cancel_order"))
    assert tool is not None, "cancel_order tool not registered"

    with _patch_exceptions():
        result = asyncio.get_event_loop().run_until_complete(
            tool.fn(account_id="a1", client_order_id="o1")
        )
    assert isinstance(result, str)
    assert "SDK boom" in result or "Server error" in result


