def test_property17_audit_logs_go_to_stderr(capsys):
    """AuditLogger writes all output to stderr, nothing to stdout."""
    config = _config()
    audit = AuditLogger(config)

    # Emit various audit events
    audit.log_tool_call("test_tool", {"key": "value"})
    audit.log_order_attempt("AAPL", "BUY", 10, "MARKET", "o1", "a1")
    audit.log_order_result("o1", True, {"status": "ok"})
    audit.log_validation_error("test_tool", "some error", {"x": 1})

    captured = capsys.readouterr()
    # stdout must be empty — all output goes to stderr
    assert captured.out == "", "Audit logs must not write to stdout"
    # stderr should contain the log lines
    assert "TOOL_CALL" in captured.err
    assert "ORDER_ATTEMPT" in captured.err
    assert "ORDER_RESULT" in captured.err
    assert "VALIDATION_ERROR" in captured.err


def test_property17_logger_handlers_target_stderr():
    """All handlers on the AuditLogger's internal logger write to stderr,
    not stdout."""
    config = _config()
    audit = AuditLogger(config)

    for handler in audit._logger.handlers:
        if isinstance(handler, logging.StreamHandler) and not isinstance(
            handler, logging.FileHandler
        ):
            assert handler.stream is sys.stderr, (
                "StreamHandler must target sys.stderr, not sys.stdout"
            )
