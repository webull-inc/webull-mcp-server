import click

from webull_openapi_mcp import __version__
from webull_openapi_mcp.config import ServerConfig, load_config, validate_config
from webull_openapi_mcp.errors import ConfigError


def _mask_secret(value: str, visible_chars: int = 4) -> str:
    """Mask a secret value, showing only the first few characters."""
    if not value:
        return "(not set)"
    if len(value) <= visible_chars:
        return "*" * len(value)
    return value[:visible_chars] + "*" * (len(value) - visible_chars)


def _is_posix() -> bool:
    """Check if running on a POSIX system."""
    return os.name == "posix"


def _build_webull_mcp_banner(config: ServerConfig) -> str:
    """Build an ASCII-only startup banner safe for stdio MCP transports."""
    width = 70
    border = "+" + "-" * (width - 2) + "+"

    def row(text: str = "") -> str:
        return f"| {text[: width - 4]:<{width - 4}} |"

    def center(text: str = "") -> str:
        return f"| {text[: width - 4]:^{width - 4}} |"

    def field(label: str, value: str) -> str:
        return row(f"{label:<11}: {value}")

    return "\n".join(
        [
            border,
            center("WEBULL OPENAPI MCP"),
            center("Model Context Protocol Server"),
            border,
            field("Version", __version__),
            field("Region", config.region_id.upper()),
            field("Environment", config.environment.upper()),
            field("Transport", "stdio"),
            border,
        ]
    )


def _print_webull_mcp_banner(config: ServerConfig) -> None:
    """Print the custom banner to stderr so stdout remains MCP-protocol clean."""
    click.echo(_build_webull_mcp_banner(config), err=True)


